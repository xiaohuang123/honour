$('.contralKnow').click(function () {
      if($(this).hasClass('A_N_P_AliL')){
          $(this).siblings('.A_N_P_Ali').not('.must').removeClass('A_N_P_Ali');
          $(this).removeClass('A_N_P_AliL');
      } else {
          $(this).siblings().not('.must').addClass('A_N_P_Ali');
          $(this).addClass('A_N_P_AliL');
      }

});

